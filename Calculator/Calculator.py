import math

class Calculator:

    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiplication(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

    def squareroot(self, a):
        return math.sqrt(a)

    def square(self, a):
        return math.pow(a, 2)